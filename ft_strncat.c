#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	char *ptr;

	ptr = s1;
	while (*ptr)
		ptr++;
	while (*s2 && n-- >= 1)
		*ptr++ = *s2++;
	*ptr = '\0';
	return (s1);
}
